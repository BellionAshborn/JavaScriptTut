// Increment Operators 

// Pre-Increment --> + 
 
var a = 10; 
var b = ++a;
console.log(b); //  here it will be changed to 11 

// Post-Increment --> + 

var a = 10; 
var b = a++;    
console.log(b); //  here it will be 10 and will not change to 11 
console.log(a); //  here it will be changed to 11 

// Pre-Decrement --> -

var a = 10; 
var b = --a;
console.log(b); //  here it will be changed to 9

// Post-Decrement --> -

var a = 10; 
var b = a--;    
console.log(b); //  here it will be 10 
console.log(a); //  here it will be changed to 9
